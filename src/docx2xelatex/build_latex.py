class LatexConfigError(ValueError):
    pass


def _latex(config: dict[str, Any]) -> dict[str, Any]:
    return config.get("latex", {})


def _tex_bool(value: Any, default: bool = False) -> bool:
    if value is None:
        return default
    return bool(value)


def validate_latex_config(config: dict[str, Any]) -> None:
    latex = _latex(config)
    engine = str(latex.get("engine", "xelatex")).lower()
    use_polyglossia = _tex_bool(latex.get("use_polyglossia"), engine == "xelatex")
    use_babel = _tex_bool(latex.get("use_babel"), False)
    if engine == "xelatex" and use_polyglossia and use_babel:
        raise LatexConfigError(
            "Invalid LaTeX config: xelatex cannot use both polyglossia and babel. "
            "Set latex.use_babel=false or latex.use_polyglossia=false."
        )


def _font_name(latex: dict[str, Any], key: str, default: str) -> str:
    return str(latex.get(key) or default).replace("\n", " ").strip()


def _language(latex: dict[str, Any]) -> str:
    return str(latex.get("main_language") or "russian")


def _other_languages(latex: dict[str, Any]) -> list[str]:
    langs = latex.get("other_languages", ["english"])
    if isinstance(langs, str):
        langs = [langs]
    return [str(lang) for lang in langs if str(lang).strip()]


def render_latex_header(config: dict[str, Any]) -> str:
    """Render the default Russian scientific XeLaTeX header.

    This header is generated from config instead of relying on Pandoc language
    variables, so XeLaTeX uses exactly one language system by default:
    polyglossia, not babel.
    """
    validate_latex_config(config)
    latex = _latex(config)
    engine = str(latex.get("engine", "xelatex")).lower()
    mainfont = _font_name(latex, "mainfont", "Times New Roman")
    sansfont = _font_name(latex, "sansfont", "Arial")
    monofont = _font_name(latex, "monofont", "Consolas")
    main_language = _language(latex)
    other_languages = _other_languages(latex)
    use_polyglossia = _tex_bool(latex.get("use_polyglossia"), engine == "xelatex")
    use_babel = _tex_bool(latex.get("use_babel"), False)
    use_unicode_math = _tex_bool(latex.get("use_unicode_math"), False)

    lines = [
        "% Generated by docx2xelatex: Russian XeLaTeX preset.",
        r"\usepackage{fontspec}",
        rf"\setmainfont{{{mainfont}}}",
        rf"\setsansfont{{{sansfont}}}",
        rf"\setmonofont{{{monofont}}}",
        "",
    ]
    if use_polyglossia:
        lines.extend(
            [
                r"\usepackage{polyglossia}",
                rf"\setmainlanguage{{{main_language}}}",
            ]
        )
        for lang in other_languages:
            lines.append(rf"\setotherlanguage{{{lang}}}")
        lines.extend(
            [
                "",
                rf"\newfontfamily\cyrillicfont{{{mainfont}}}[Script=Cyrillic]",
                rf"\newfontfamily\cyrillicfontsf{{{sansfont}}}[Script=Cyrillic]",
                rf"\newfontfamily\cyrillicfonttt{{{monofont}}}[Script=Cyrillic]",
                "",
            ]
        )
    elif use_babel:
        languages = ",".join([*other_languages, f"main={main_language}"])
        lines.extend([rf"\usepackage[{languages}]{{babel}}", ""])

    lines.extend(
        [
            r"\usepackage{amsmath}",
            r"\usepackage{amssymb}",
            r"\usepackage{mathtools}",
        ]
    )
    if use_unicode_math:
        lines.append(r"\usepackage{unicode-math}")
    lines.extend(
        [
            r"\usepackage{upgreek,tensor,xfrac}",
            r"\providecommand{\Uptheta}{\Theta}",
            r"\providecommand{\Upomega}{\Omega}",
            r"\providecommand{\Updelta}{\Delta}",
            r"\providecommand{\Upphi}{\Phi}",
            r"\providecommand{\Upsigma}{\Sigma}",
            r"\sloppy",
            "",
        ]
    )
    return "\n".join(lines)


def _write_header(workdir: Path, config: dict[str, Any]) -> Path:
    header = workdir / "header-xelatex-ru.tex"
    write_text(header, render_latex_header(config))
    return header


def _pandoc_latex_variables(config: dict[str, Any]) -> list[str]:
    latex = _latex(config)
    engine = str(latex.get("engine", "xelatex")).lower()
    use_polyglossia = _tex_bool(latex.get("use_polyglossia"), engine == "xelatex")
    variables = [
        f"documentclass={latex.get('documentclass', 'article')}",
        f"fontsize={latex.get('fontsize', '12pt')}",
    ]
    # With XeLaTeX+polyglossia, language and fonts are owned by the generated
    # header. Passing lang/mainfont/etc. through Pandoc can make Pandoc's
    # default template load babel or duplicate font setup.
    if not (engine == "xelatex" and use_polyglossia):
        variables.extend(
            [
                f"mainfont={latex.get('mainfont', 'Times New Roman')}",
                f"sansfont={latex.get('sansfont', 'Arial')}",
                f"monofont={latex.get('monofont', 'Consolas')}",
                f"lang={latex.get('lang', 'ru-RU')}",
            ]
        )
    return variables


def pandoc_final_to_tex(workdir: str | Path, config: dict[str, Any], force: bool = False) -> Path:
    validate_latex_config(config)
    wp = WorkPaths.from_workdir(workdir)
    wp.ensure()
    if wp.final_tex.exists() and not force:
        return wp.final_tex
    if not wp.final_md.exists():
        raise FileNotFoundError(f"final.md not found: {wp.final_md}")
    if wp.manifest_json.exists():
        final_md_text = wp.final_md.read_text(encoding="utf-8", errors="replace")
        manifest = load_manifest(workdir)
        unresolved = scan_forbidden_formula_refs(final_md_text, manifest, workdir, config)
        if unresolved:
            from .utils import write_json

            write_json(wp.root / "merge-unresolved.json", unresolved)
            raise MergeInvariantError("Build refused: final.md still contains WMF/EMF formula references; run merge --strict", unresolved)
    cmd = [
        "pandoc",
        str(wp.final_md),
        "-f",
        "markdown+raw_tex+tex_math_single_backslash",
        "-t",
        "latex",
        "--standalone",
        "-H",
        str(_write_header(wp.root, config)),
    ]
    for variable in _pandoc_latex_variables(config):
        cmd.extend(["-V", variable])
    cmd.extend(["--resource-path", str(wp.root), "-o", str(wp.final_tex)])
    proc = run_command(cmd)
    if proc.returncode != 0:
        log = wp.root / "pandoc-final.log"
        tail = (proc.stdout + "\n" + proc.stderr)[-3000:]
        write_text(log, f"CMD: {' '.join(proc.cmd)}\nRETURN: {proc.returncode}\nTIMED_OUT: {proc.timed_out}\n\nSTDOUT:\n{proc.stdout}\n\nSTDERR:\n{proc.stderr}\n")
        raise RuntimeError(f"pandoc final build failed ({proc.returncode}); log={log}\n{tail}")
    return wp.final_tex


def compile_tex(tex_path: str | Path, config: dict[str, Any], timeout: int = 240) -> dict[str, Any]:
    validate_latex_config(config)
    tex = Path(tex_path)
    latex = _latex(config)
    engine = str(latex.get("engine", "xelatex"))
    if shutil.which(engine) is None:
        return {"status": "failed", "pdf": None, "log": None, "error": f"{engine} not found"}
    args = [engine, "-interaction=nonstopmode"]
    if bool(latex.get("halt_on_error", False)):
        args.append("-halt-on-error")
    args.append(tex.name)
    last = run_command(args, cwd=tex.parent, timeout=timeout)
    runs = [last]
    rerun_markers = (
        "Rerun to get cross-references right",
        "Label(s) may have changed",
        "There were undefined references",
        "Rerun LaTeX",
    )
    output = f"{last.stdout}\n{last.stderr}"
    if last.returncode == 0 and any(marker in output for marker in rerun_markers):
        last = run_command(args, cwd=tex.parent, timeout=timeout)
        runs.append(last)
    pdf = tex.with_suffix(".pdf")
    log = tex.with_suffix(".log")
    ok = pdf.exists() and last is not None and last.returncode == 0
    err = None if ok else ((last.stdout + "\n" + last.stderr)[-3000:] if last else "xelatex was not run")
    build_log = tex.with_suffix(".build.log")
    if runs:
        chunks = []
        for idx, run in enumerate(runs, start=1):
            chunks.append(
                f"=== xelatex run {idx} ===\n"
                f"CMD: {' '.join(run.cmd)}\nRETURN: {run.returncode}\nTIMED_OUT: {run.timed_out}\n"
                f"\nSTDOUT:\n{run.stdout}\n\nSTDERR:\n{run.stderr}\n"
            )
        write_text(build_log, "\n".join(chunks))
    return {"status": "ok" if ok else "failed", "pdf": str(pdf) if pdf.exists() else None, "log": str(log) if log.exists() else None, "build_log": str(build_log) if build_log.exists() else None, "error": err}


def compile_final_pdf(workdir: str | Path, config: dict[str, Any]) -> dict[str, Any]:
    wp = WorkPaths.from_workdir(workdir)
    latex = _latex(config)
    if not latex.get("build_pdf", True):
        return {"status": "skipped", "pdf": None, "log": None, "error": None}
    if not wp.final_tex.exists():
        raise FileNotFoundError(f"final.tex not found: {wp.final_tex}")
    return compile_tex(wp.final_tex, config)


def minimal_russian_latex_document(config: dict[str, Any]) -> str:
    latex = _latex(config)
    documentclass = latex.get("documentclass", "article")
    fontsize = latex.get("fontsize", "12pt")
    return "\n".join(
        [
            rf"\documentclass[{fontsize}]{{{documentclass}}}",
            render_latex_header(config),
            r"\begin{document}",
            "Тест русского текста.",
            r"\texttt{Тест моноширинного русского текста: АБВ абв ёЁ}",
            r"\[",
            r"a^2 + b^2 = c^2",
            r"\]",
            r"\end{document}",
            "",
        ]
    )


def run_latex_smoke_test(workdir: str | Path, config: dict[str, Any]) -> dict[str, Any]:
    validate_latex_config(config)
    wp = WorkPaths.from_workdir(workdir)
    wp.ensure()
    out_dir = wp.root / "latex-test"
    out_dir.mkdir(parents=True, exist_ok=True)
    tex = out_dir / "russian-xelatex-test.tex"
    write_text(tex, minimal_russian_latex_document(config))
    result = compile_tex(tex, config)
    return {"tex": str(tex), **result}


def _fc_match(font: str) -> dict[str, Any]:
    if shutil.which("fc-match") is None:
        return {"method": "fc-match", "available": False, "ok": None, "match": None}
    proc = run_command(["fc-match", font], timeout=10)
    out = proc.stdout.strip()
    return {"method": "fc-match", "available": True, "ok": proc.returncode == 0 and bool(out), "match": out or None, "error": proc.stderr.strip() or None}


def latex_doctor_checks(config: dict[str, Any]) -> dict[str, Any]:
    latex = _latex(config)
    engine = str(latex.get("engine", "xelatex"))
    report: dict[str, Any] = {
        "config_ok": True,
        "config_error": None,
        "engine": engine,
        "engine_path": shutil.which(engine),
        "engine_ok": shutil.which(engine) is not None,
        "use_polyglossia": _tex_bool(latex.get("use_polyglossia"), engine.lower() == "xelatex"),
        "use_babel": _tex_bool(latex.get("use_babel"), False),
        "use_unicode_math": _tex_bool(latex.get("use_unicode_math"), False),
        "fonts": {},
        "monofont_cyrillic_compile": None,
        "minimal_russian_compile": None,
    }
    try:
        validate_latex_config(config)
    except LatexConfigError as exc:
        report["config_ok"] = False
        report["config_error"] = str(exc)
        return report
    fonts = {
        "mainfont": _font_name(latex, "mainfont", "Times New Roman"),
        "sansfont": _font_name(latex, "sansfont", "Arial"),
        "monofont": _font_name(latex, "monofont", "Consolas"),
    }
    report["fonts"] = {key: {"name": value, **_fc_match(value)} for key, value in fonts.items()}
    if report["engine_ok"]:
        with tempfile.TemporaryDirectory(prefix="docx2xelatex-latex-doctor-") as tmp:
            smoke = run_latex_smoke_test(tmp, config)
            report["monofont_cyrillic_compile"] = smoke
            report["minimal_russian_compile"] = smoke
    return report


def build(workdir: str | Path, config: dict[str, Any], force: bool = False) -> dict[str, Any]:
    tex = pandoc_final_to_tex(workdir, config, force=force)
    pdf_result = compile_final_pdf(workdir, config)
    return {"tex": str(tex), **pdf_result}
